            <ul class="ex_menu">
            <li><h2 class="intelletters">Core i3</h2>
            <p>Positioned as entry-level processors, Core i3 CPUs are generally suitable for everyday tasks such as web
                browsing, office applications, and light multitasking. They usually have a lower core count and lack
                some of the more advanced features found in higher-tier CPUs.</li>
            </p>
            <p class="intelletters">Core i5</p>
            <p>This CPU series is targeted at mainstream users and offers a good balance of performance and value.
                These processors typically have more cores than Core i3 models and support features like Turbo Boost,
                which dynamically increases clock speeds for improved performance.</p>
            <p class="intelletters">Core i7</p>
            <p> These CPUs are designed for power users, gamers, and content creators who need higher performance
                capabilities. They come with more cores, higher clock speeds, and often include Hyper-Threading (SMT)
                technology, allowing each physical core to handle two threads simultaneously.</p>
            <p class="intelletters">Core i9</p>
            <p>Positioned as Intel's top-of-the-line consumer-grade processors, Core i9 CPUs offer the best performance
                for demanding tasks like video editing, 3D rendering, and gaming. They have the highest core counts, the
                fastest clock speeds, and support advanced technologies like Hyper-Threading.</p>

